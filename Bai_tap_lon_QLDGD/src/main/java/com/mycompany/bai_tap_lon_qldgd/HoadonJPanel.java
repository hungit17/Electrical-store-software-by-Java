    private void btn_updatehdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updatehdActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tmodel = (DefaultTableModel) table_dshoadon.getModel();
        if(table_dshoadon.getSelectedRowCount()==1){
            if(jtx_tenkhachhang.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên khách hàng !");
            jtx_tenkhachhang.requestFocus();
        }else if(jtx_sdt.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại !");
            jtx_sdt.requestFocus();
        }else if(jtx_diachi.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ !");
            jtx_diachi.requestFocus();
        }else{
            String tenkh = jtx_tenkhachhang.getText();
            String sdt = jtx_sdt.getText();
            String diachi = jtx_diachi.getText();
            String trangthai = jcombo_trangthai.getItemAt(jcombo_trangthai.getSelectedIndex());

            tmodel.setValueAt(tenkh, table_dshoadon.getSelectedRow(), 1);
            tmodel.setValueAt(sdt, table_dshoadon.getSelectedRow(), 2);
            tmodel.setValueAt(diachi, table_dshoadon.getSelectedRow(), 3);
            tmodel.setValueAt(trangthai, table_dshoadon.getSelectedRow(), 5);
            jtx_mahoadon.setText("");
            jtx_tenkhachhang.setText("");
            jtx_sdt.setText("");
            jtx_diachi.setText("");
            jtx_ngaytao.setText("");
            jcombo_trangthai.setSelectedIndex(0);
            chuoi="";
            jtx_tongthanhtien.setText("");
            jtx_mahoadon.requestFocus();
            JOptionPane.showMessageDialog(this, "Cập nhật hoá đơn thành công");
        }
       
        }
    }//GEN-LAST:event_btn_updatehdActionPerformed

