    private void btnLaphoadonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLaphoadonActionPerformed
        // TODO add your handling code here:
        if(jtx_mahoadon.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã hoá đơn !");
            jtx_mahoadon.requestFocus();
        }else if(jtx_tenkhachhang.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên khách hàng !");
            jtx_tenkhachhang.requestFocus();
        }else if(jtx_sdt.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại !");
            jtx_sdt.requestFocus();
        }else if(jtx_sdt.getText().toString().length()!=10){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại la 10 số !");
            jtx_sdt.requestFocus();
        }else if(jtx_diachi.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ !");
            jtx_diachi.requestFocus();
        }else if(chuoi==""){
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn hoá đơn này chưa thanh toán ?");
            if(chon==JOptionPane.YES_OPTION){
                chuoi="Chưa thanh toán";
                jtx_tongthanhtien.requestFocus();
            }else{
                JOptionPane.showMessageDialog(this, "Vui lòng chọn trạng thái hoá đơn");
                jtx_ngaytao.requestFocus();
            }
            
        }else if(jtx_tongthanhtien.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhấn nút Tổng thành tiền");
            jtx_ngaytao.requestFocus();
        }else if(Integer.parseInt(jtx_tongthanhtien.getText())==0){
            JOptionPane.showMessageDialog(this, "Giỏ hàng rỗng, vui lòng thêm mặt hàng vào giỏ hàng để lập hoá đơn !");
            jtx_ngaytao.requestFocus();
        }else{
              DefaultTableModel tmodel = (DefaultTableModel) table_dshoadon.getModel();
              int i,found =0;
              for(i=0;i<table_dshoadon.getRowCount();i++){
                  if(tmodel.getValueAt(i, 0).toString().equals(jtx_mahoadon.getText())==true){
                      found=1;
                      break;
                  }
              }
              if(found==0){
                  String data[]={jtx_mahoadon.getText(),jtx_tenkhachhang.getText(), jtx_sdt.getText(),jtx_diachi.getText(),jtx_ngaytao.getText(),chuoi,jtx_tongthanhtien.getText()};
                  tmodel.addRow(data);
                  JOptionPane.showMessageDialog(this, "Lập hoá đơn thành công !");
                  jtx_mahoadon.setText("");
                  jtx_tenkhachhang.setText("");
                  jtx_sdt.setText("");
                  jtx_diachi.setText("");
                  jtx_ngaytao.setText("");
                  jcombo_trangthai.setSelectedIndex(0);
                  chuoi="";
                  jtx_tongthanhtien.setText("");
                  jtx_mahoadon.requestFocus();
                  DefaultTableModel ghmodel = (DefaultTableModel) table_Giohang.getModel();
                  ghmodel.setRowCount(0);
              }else{
                  JOptionPane.showMessageDialog(this, "Mã hoá đơn đã tồn tại, vui lòng nhập lại mã hoá đơn !");
                  jtx_mahoadon.requestFocus();
              }
              
              
        }
    }//GEN-LAST:event_btnLaphoadonActionPerformed

