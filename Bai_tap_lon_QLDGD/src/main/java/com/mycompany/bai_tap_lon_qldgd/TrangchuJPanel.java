    private void btn_sortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sortActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_mathang.getModel();
        for(int i=0;i<model.getRowCount()-1;i++){
            for(int j=i+1;j<model.getRowCount();j++){
                String c1 = model.getValueAt(i, 1).toString();
                String c2 = model.getValueAt(j, 1).toString();
                if(c1.compareTo(c2)>0){
                    // lay thong tin 2 dong
                    String mahang1 = model.getValueAt(i, 0).toString();
                    String mahang2 = model.getValueAt(j, 0).toString();
                    String tenhang1 =model.getValueAt(i, 1).toString();
                    String tenhang2 =model.getValueAt(j, 1).toString();
                    String loai1 = model.getValueAt(i, 2).toString();
                    String loai2 = model.getValueAt(j, 2).toString();
                    String soluong1 = model.getValueAt(i, 3).toString();
                    String soluong2 = model.getValueAt(j, 3).toString();
                    String dvtinh1 = model.getValueAt(i, 4).toString();
                    String dvtinh2 = model.getValueAt(j, 4).toString();
                    String dongia1 = model.getValueAt(i, 5).toString();
                    String dongia2 = model.getValueAt(j, 5).toString();
                    // sua thong tin 2 dong voi nhau
                    model.setValueAt(mahang1, j, 0);
                    model.setValueAt(mahang2, i, 0);
                    model.setValueAt(tenhang1, j, 1);
                    model.setValueAt(tenhang2, i, 1);
                    model.setValueAt(loai1, j, 2);
                    model.setValueAt(loai2, i, 2);
                    model.setValueAt(soluong1, j, 3);
                    model.setValueAt(soluong2, i, 3);
                    model.setValueAt(dvtinh1, j, 4);
                    model.setValueAt(dvtinh2, i, 4);
                    model.setValueAt(dongia1, j, 5);
                    model.setValueAt(dongia2, i, 5);
                }
                
            }
        }
    }//GEN-LAST:event_btn_sortActionPerformed


