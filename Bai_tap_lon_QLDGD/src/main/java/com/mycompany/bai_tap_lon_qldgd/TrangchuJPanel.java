    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(jtxmahang.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào mã hàng !");
            jtxmahang.requestFocus();
        }else if(jtxtenhang.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào tên hàng");
            jtxtenhang.requestFocus();
        }else if(jtxsoluong.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào số lượng !");
            jtxsoluong.requestFocus();
        }else if(Long.parseLong(jtxsoluong.getText())<1){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng > 0 !");
            jtxsoluong.requestFocus();
        }else if(jtxdvtinh.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn vị tính !");
            jtxdvtinh.requestFocus();
        }else if(jtxdongia.getText().isEmpty()==true){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn giá !");
            jtxdongia.requestFocus();
        }else if(Long.parseLong(jtxdongia.getText())<1){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn giá > 0 !");
            jtxdongia.requestFocus();
        }else{
            if(chuoi==""){
                chuoi="Thiết bị điện";
            }
            int i,found=0;
            DefaultTableModel tblmodel = (DefaultTableModel) table_mathang.getModel() ;
            for(i=0;i<table_mathang.getRowCount();i++){
                if(tblmodel.getValueAt(i, 0).toString().equals(jtxmahang.getText())==true){
                    found=1;
                    break;
                }
            }
            if(found==0){
                String data[] = {
                    jtxmahang.getText(),jtxtenhang.getText(),chuoi,jtxsoluong.getText(),jtxdvtinh.getText(),jtxdongia.getText()
                };
                tblmodel.addRow(data);
                JOptionPane.showMessageDialog(this, "Thêm vào danh sách thành công !");
                jtxmahang.setText("");
                jtxtenhang.setText("");
                jcbloai.setSelectedIndex(0);
                chuoi="";
                jtxsoluong.setText("");
                jtxdongia.setText("");
                jtxdvtinh.setText("");
                jtxmahang.requestFocus();
            }else{
                JOptionPane.showMessageDialog(this, "Mã hàng đã tồn tại trong danh sách, vui lòng nhập lại !!!");
                jtxmahang.requestFocus();
            }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) table_mathang.getModel() ;
        if(table_mathang.getSelectedRowCount()==1){
            if(jtxtenhang.getText().isEmpty()==true){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập vào tên hàng");
               jtxtenhang.requestFocus();
           }else if(jtxsoluong.getText().isEmpty()==true){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập vào số lượng !");
               jtxsoluong.requestFocus();
           }else if(Long.parseLong(jtxsoluong.getText())<0){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng >= 0 !");
               jtxsoluong.requestFocus();
           }else if(jtxdvtinh.getText().isEmpty()==true){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn vị tính !");
               jtxdvtinh.requestFocus();
           }else if(jtxdongia.getText().isEmpty()==true){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn giá !");
               jtxdongia.requestFocus();
           }else if(Long.parseLong(jtxdongia.getText())<1){
               JOptionPane.showMessageDialog(this, "Vui lòng nhập vào đơn giá > 0 !");
               jtxdongia.requestFocus();
           }else{
               
               String mahang = tblmodel.getValueAt(table_mathang.getSelectedRow(), 0).toString();
               String tenhang = jtxtenhang.getText();
               String loai = chuoi;
               String soluong = jtxsoluong.getText();
               String dvtinh = jtxdvtinh.getText();
               String dongia = jtxdongia.getText();
               tblmodel.setValueAt(tenhang, table_mathang.getSelectedRow(), 1);
               tblmodel.setValueAt(loai, table_mathang.getSelectedRow(), 2);
               tblmodel.setValueAt(soluong, table_mathang.getSelectedRow(), 3);
               tblmodel.setValueAt(dvtinh, table_mathang.getSelectedRow(), 4);
               tblmodel.setValueAt(dongia, table_mathang.getSelectedRow(), 5);
               JOptionPane.showMessageDialog(this, "Đã sửa thành công mặt hàng có mã  '"+mahang+"'");
               jtxmahang.setText("");
                jtxtenhang.setText("");
                jcbloai.setSelectedIndex(0);
                chuoi="";
                jtxsoluong.setText("");
                jtxdongia.setText("");
                jtxdvtinh.setText("");
                jtxmahang.requestFocus();
               
           }
        }else{
            JOptionPane.showMessageDialog(this, "Chỉ được phép sửa mỗi lần 1 thông tin mặt h");
        }

    }//GEN-LAST:event_btnEditActionPerformed

